const html = document.documentElement;
const toggle = document.querySelector("[data-lang-toggle]");
const themeToggle = document.querySelector("[data-theme-toggle]");
const languageOptions = document.querySelectorAll("[data-lang-option]");
const themeOptions = document.querySelectorAll("[data-theme-option]");
const translatable = document.querySelectorAll("[data-i18n]");
const revealItems = document.querySelectorAll("[data-reveal]");
const cursorField = document.querySelector(".cursor-field");
document.body.classList.add("js-enabled");

function setLanguage(lang) {
  html.lang = lang;
  translatable.forEach((node) => {
    const key = node.dataset.i18n;
    if (copy[lang][key]) {
      node.textContent = copy[lang][key];
    }
  });
  languageOptions.forEach((option) => {
    option.classList.toggle("is-active", option.dataset.langOption === lang);
  });
  try {
    localStorage.setItem("systemmag-lang", lang);
  } catch {
    // Some locked-down preview contexts block localStorage.
  }
}

toggle?.addEventListener("click", () => {
  const next = html.lang === "fr" ? "en" : "fr";
  setLanguage(next);
});

function storedValue(key) {
  try {
    return localStorage.getItem(key);
  } catch {
    return null;
  }
}

function setTheme(theme) {
  const resolvedTheme = theme === "light" ? "light" : "dark";
  html.dataset.theme = resolvedTheme;
  themeOptions.forEach((option) => {
    option.classList.toggle("is-active", option.dataset.themeOption === resolvedTheme);
  });
  try {
    localStorage.setItem("systemmag-theme", resolvedTheme);
  } catch {
    // Some locked-down preview contexts block localStorage.
  }
}

themeToggle?.addEventListener("click", () => {
  const next = html.dataset.theme === "light" ? "dark" : "light";
  setTheme(next);
});

const preferredLanguage = storedValue("systemmag-lang");
const storedTheme = storedValue("systemmag-theme");
const prefersLight = window.matchMedia?.("(prefers-color-scheme: light)").matches;
setLanguage(preferredLanguage === "en" ? "en" : "fr");
setTheme(storedTheme || (prefersLight ? "light" : "dark"));

const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.16, rootMargin: "0px 0px -6% 0px" }
);


function revealVisibleNow() {
  revealItems.forEach((item) => {
    const rect = item.getBoundingClientRect();
    if (rect.top < window.innerHeight * 0.96 && rect.bottom > 0) {
      item.classList.add("is-visible");
    }
  });
}

window.addEventListener("load", revealVisibleNow);
window.addEventListener("hashchange", () => requestAnimationFrame(revealVisibleNow));
window.addEventListener("scroll", revealVisibleNow, { passive: true });
revealVisibleNow();
revealItems.forEach((item, index) => {
  item.style.transitionDelay = `${Math.min(index * 42, 260)}ms`;
  observer.observe(item);
});

