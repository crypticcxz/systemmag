const scrollVideo = document.querySelector("[data-scroll-video]");
const cues = document.querySelectorAll(".cue");
let videoReady = false;
let rafId = 0;

function videoRange() {
  const firstSection = document.querySelector(".hero");
  const thirdSection = document.querySelector(".products");
  const start = firstSection ? firstSection.offsetTop : 0;
  const end = thirdSection ? thirdSection.offsetTop - window.innerHeight * 0.08 : window.innerHeight * 2;
  return {
    start,
    end: Math.max(start + 1, end),
  };
}

function regionProgress() {
  const { start, end } = videoRange();
  const progress = (window.scrollY - start) / (end - start);
  return Math.min(1, Math.max(0, progress));
}

function setCue(progress) {
  if (!cues.length) return;
  const index = Math.min(cues.length - 1, Math.floor(progress * cues.length));
  cues.forEach((cue, cueIndex) => {
    cue.classList.toggle("is-active", cueIndex === index);
  });
}

function scrubVideo() {
  rafId = 0;
  const progress = regionProgress();
  const { end } = videoRange();
  const pastVideoRange = window.scrollY >= end;
  document.body.classList.toggle("past-video-range", pastVideoRange);
  setCue(progress);
  if (!scrollVideo || !videoReady || !Number.isFinite(scrollVideo.duration)) return;
  const targetTime = progress * Math.max(0.01, scrollVideo.duration - 0.04);
  if (Math.abs(scrollVideo.currentTime - targetTime) > 0.025) {
    scrollVideo.currentTime = targetTime;
  }
}

function requestScrub() {
  if (!rafId) rafId = requestAnimationFrame(scrubVideo);
}

if (scrollVideo) {
  scrollVideo.addEventListener("loadedmetadata", () => {
    videoReady = scrollVideo.duration > 0;
    document.body.classList.toggle("has-scroll-video", videoReady);
    requestScrub();
  });

  scrollVideo.addEventListener("error", () => {
    videoReady = false;
    document.body.classList.remove("has-scroll-video");
  });

  scrollVideo.load();
  window.addEventListener("scroll", requestScrub, { passive: true });
  window.addEventListener("resize", requestScrub);
  requestScrub();
}




const marketScene = document.querySelector(".applications");
const marketGallery = document.querySelector(".market-gallery");
const marketCards = document.querySelectorAll(".market-card");
let marketRafId = 0;

function updateMarketScene() {
  marketRafId = 0;
  if (!marketScene || !marketGallery || !marketCards.length) return;

  const sceneRect = marketScene.getBoundingClientRect();
  const viewport = window.innerHeight || 1;
  const travel = Math.max(1, sceneRect.height - viewport);
  const progress = Math.min(1, Math.max(0, -sceneRect.top / travel));
  const maxIndex = marketCards.length - 1;
  const floatIndex = progress * maxIndex;
  const activeIndex = Math.round(floatIndex);

  marketScene.style.setProperty("--scene-progress", progress.toFixed(4));
  marketGallery.style.setProperty("--scene-progress", progress.toFixed(4));
  marketGallery.style.setProperty("--scene-index", floatIndex.toFixed(4));

  marketCards.forEach((card, index) => {
    const distance = index - floatIndex;
    const abs = Math.abs(distance);
    const active = Math.max(0, 1 - abs);
    const near = Math.max(0, 1 - abs / 1.6);
    const direction = Math.sign(distance || 0);
    card.style.setProperty("--card-distance", distance.toFixed(4));
    card.style.setProperty("--card-abs", abs.toFixed(4));
    card.style.setProperty("--card-active", active.toFixed(4));
    card.style.setProperty("--card-near", near.toFixed(4));
    card.style.setProperty("--card-x", (distance * 118).toFixed(2) + "%");
    card.style.setProperty("--card-y", (Math.min(abs, 1.8) * 18).toFixed(2) + "%");
    card.style.setProperty("--card-tilt", (direction * Math.min(abs, 1) * -9).toFixed(2) + "deg");
    card.style.setProperty("--card-glow-x", (50 + distance * -28).toFixed(2) + "%");
    card.style.setProperty("--liquid-progress", active.toFixed(4));
    card.style.setProperty("--liquid-center", near.toFixed(4));
    card.classList.toggle("is-scene-active", index === activeIndex);
    card.classList.toggle("is-liquid-active", near > 0.28);
  });
}

function requestMarketScene() {
  if (!marketRafId) marketRafId = requestAnimationFrame(updateMarketScene);
}

if (marketCards.length) {
  document.body.classList.add("js-markets-ready");
  window.addEventListener("scroll", requestMarketScene, { passive: true });
  window.addEventListener("resize", requestMarketScene);
  window.addEventListener("load", requestMarketScene);
  requestMarketScene();
}



